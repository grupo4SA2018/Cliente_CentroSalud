    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        String fecha = tf_fechac.getText();
        int realizada;
        String dpi = tf_dpic.getText();
        String doctor = combo_doc.getSelectedItem().toString().split("-")[0];
        if(rb_citasi.isSelected()){
            realizada = 1;
        }else if(rb_citano.isSelected()){
            realizada = 0;
        }else{
            excepcion("indique si ya realizó la cita");
            return;
        }
        try{
            String idpaciente = obtenerIdPaciente(dpi);
            if(idpaciente.contains("error")){
                excepcion("Problemas con el DPI");
                return;
            }
            //System.out.println(idpaciente);
            
            int doc = Integer.parseInt(doctor);
            int pac = Integer.parseInt(idpaciente);
            String reg = registroCita(fecha, realizada, pac, doc);
            if(reg.contains("error")){
                excepcion("error insertando");
                return;
            }
            JOptionPane.showMessageDialog(null,"Registro exitoso!");
            
        }
        catch(Exception ex){
            excepcion(ex.toString());
        }
        
        
        
        
    }//GEN-LAST:event_jButton6ActionPerformed

