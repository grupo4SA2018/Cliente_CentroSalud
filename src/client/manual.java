        private void llenado_doctores(){
        combo_doc.removeAllItems();
        try{
            String doctores = consultarDoctores();
            
            //System.out.println(doctores);
            JSONObject jObject = new JSONObject(doctores);
            Iterator<?> keys = jObject.keys();
            ArrayList<String> lista = new ArrayList<>();
            while( keys.hasNext() ) {
                String key = (String)keys.next();
                if ( jObject.get(key) instanceof JSONObject ) {
                    System.out.println(key);
                    JSONObject child = (JSONObject) jObject.get(key);
                    String n = child.get("nombre").toString();
                    String id = key;
                    lista.add(id+"-"+n);
                }
            }
            
            for (String s: lista){
                combo_doc.addItem(s);
                System.out.println(s);
            }
        }
        catch(Exception ex){
            System.out.println(ex);
        }
    }
        
